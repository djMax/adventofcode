const solve = (input, optimalDigit) => input
  .split('\n')
  .reduce((acc, line) => {
    if (line.startsWith('inp ')) {
      acc.push([]);
    } else {
      acc[acc.length - 1].push(line);
    }
    return acc;
  }, [])
  .map((l) => [Number(l[4].split(' ')[2]), Number(l[14].split(' ')[2])])
  .reduce(({ num, stack }, [addx, addy], index) => {
    if (addx > 0) {
      return { num, stack: [...stack, [addy, index]] };
    } else {
      const [addyPop, indexPop] = stack.pop();
      let digit = optimalDigit;
      if (optimalDigit === 9) {
        while (digit + addyPop + addx > optimalDigit) {
          digit--;
        }
      } else {
        while (digit + addyPop + addx < optimalDigit) {
          digit++;
        }
      }
      const digits = num.split('');
      digits[indexPop] = digit;
      digits[index] = digit + addyPop + addx;
      return { num: digits.join(''), stack };
    }
  }, { num: '..............', stack: [] }).num;

