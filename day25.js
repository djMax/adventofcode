const read = input => input.split('\n').map(line => line.split(''));
const viz = board => board.map(row => row.join('')).join('\n');

const getRMoves = (board) => {
  const moves = [];
  board.forEach((row, rx) => row.forEach((cell, cx) =>
    cell === '>' && row[(cx + 1) % row.length] === '.' && moves.push(['R', rx, cx])));
  return moves;
};

const getDMoves = (board) => {
  const moves = [];
  Array(board[0].length).fill(0).forEach((_, cx) => board.forEach((row, rx) =>
    row[cx] === 'v' && board[(rx + 1) % board.length][cx] === '.' && moves.push(['D', rx, cx])));
  return moves;
};

const doMove = (board, [direction, rx, cx]) => {
  if (direction === 'R') {
    board[rx][cx] = '.';
    board[rx][(cx + 1) % board[0].length] = '>';
  } else {
    board[rx][cx] = '.';
    board[(rx + 1) % board.length][cx] = 'v';
  }
};

const movesUntilNone = (input) => {
  const board = read(input);
  let rounds = 0;
  while (true) {
    rounds += 1;
    const rmoves = getRMoves(board);
    rmoves.forEach(move => doMove(board, move));
    const dmoves = getDMoves(board);
    dmoves.forEach(move => doMove(board, move));
    if (rmoves.length === 0 && dmoves.length === 0) {
      return rounds;
    }
  }
}

